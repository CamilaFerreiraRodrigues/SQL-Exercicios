--- C) SELECIONE O SOBRENOME, PRIMEIRO NOME, DEPARTAMENTO, DATA DE ADMISSÃO E SALÁRIOS DE TODOS QUE GANHAM MAIS QUE 5000 REAIS
SELECT NOME, SOBRENOME, DEPT, DATA_ADM, SALARIO FROM EMPREGADOS 
WHERE SALARIO > 5000;

---- D) QUAL A MÉDIA DE SALÁRIO DOS FUNCIONÁRIOS
SELECT  AVG(SALARIO) AS 'MÉDIA DE SAÁRIO' FROM EMPREGADOS;

---- E) QUANTOS FUNCIONÁRIOS PERTECEM AO DEPARTAMENTO DE TI
SELECT COUNT(DEPT) AS 'FUNCIONARIOS DE TI' FROM EMPREGADOS 
WHERE DEPT = 'TI';

---- F) QUANTOS FUNCIONÁRIOS TEMOS COM CARGO 'ANALISTA'
SELECT COUNT(CARGO) AS 'QTD DE ANALISTAS' FROM EMPREGADOS 
WHERE CARGO = 'ANALISTA';

----G) QUAL A SOMATÓRIA DOS SALÁRIOS
SELECT SUM(SALARIO) AS 'SOMATÓRIA DOS SALÁRIOS' FROM EMPREGADOS; 

----H) QUAL O VALOR DO MAIOR SALÁRIO?
SELECT MAX(SALARIO) AS 'MAIOR SALÁRIO' FROM EMPREGADOS;  

----I) QUAL O VALOR DO MENOR SALÁRIO?
SELECT MIN(SALARIO) AS 'MENOR SALÁRIO' FROM EMPREGADOS; 
 
----J) ATRIBUIR UM AUMENTO DE 5% AOS FUNCIONÁRIOS DE TI
UPDATE EMPREGADOS 
SET SALARIO = SALARIO*1.05 WHERE DEPT = 'TI';

--- K) SELECIONAR OS CARGOS (SEM DUPLICIDADES)
